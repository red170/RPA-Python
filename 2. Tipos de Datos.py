# Ejemplos de tipos de datos básicos en Python

# Enteros
numero_entero = 10
print(numero_entero)  # Salida: 10

# Números de punto Flotante (Decimales)
numero_flotante = 10.5
print(numero_flotante)  # Salida: 10.5

# Cadenas de texto
cadena_texto = "Hola, Mundo!"
print(cadena_texto)  # Salida: Hola, Mundo!

# Booleanos
es_verdadero = True
es_falso = False
print(es_verdadero)  # Salida: True
print(es_falso)      # Salida: False

#Verificar el Tipo de Dato
print(type(numero_entero))
print(type(numero_flotante))
print(type(cadena_texto))
print(type(es_falso))